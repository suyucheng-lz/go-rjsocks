func byteReverse(base byte) byte {
	result := byte(0)
	for i := 0; i < 8; i++ {
		result <<= 1
		result |= base & 0x01
		base >>= 1
	}
	return ^result
}

func symEncode(data []byte) {
	for i := 0; i < 4; i++ {
		data[i] = byteReverse(data[i])
	}
}

